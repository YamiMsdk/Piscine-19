#include <unistd.h>

void ft_print_comb(void)
{
    char a;
    char b;


    a ='0';
    while (a <='9')
    {
        b = '1';
        while (b <= '9')
        {
              
                if(a != b) 
                {
                  write(1, &a, 1);
                  write(1, &b, 1);
                  write(1, ", ", 2);
                }
                 b++;
                }
              
        a++;
    }
}


int main(void)
{
    ft_print_comb();
}
