#include <unistd.h>

void	ft_write(int grid[4][4])
{
	char	a;
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (i < 4)
	{
		while (j < 4)
		{
			a = grid[i][j++] + '0';
			write(1, &a, 1);
			if (j < 4)
				write(1, " ", 1);
		}
		j = 0;
		write(1, "\n", 1);
		i++;
	}
}
